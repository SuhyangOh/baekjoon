import java.util.*;
 public class Baekjoon10952 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        while (a != 0) {
            int b = sc.nextInt();
            System.out.println(a + b);
            a = sc.nextInt();
        }

    }
}